package com.string_manipulation;

/*
 Length =
 Length is a variable used to find length/size of Array
 
  Length()=
  Length() is a method used to find a Length of String
 */
public class Length_vs_LengthOf {

	public static void main(String[] args) {

		// Length
		 int [] arr=new int[100];
		 System.out.println(arr.length);
		 
		 
			// Length of
		 String str="Sajid";
		 System.out.println(str.length());
	}

}
