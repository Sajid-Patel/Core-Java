package com.arrays;

public class Length_vs_Lengthof {

	public static void main(String[] args) {

		// Length --> is a variable used to find Length of Arrays
		int[] a = new int[20];
		System.out.println(a.length);

		// Length() --> is a method used to find Length of Strings
		String str = "Sajid";
		System.out.println(str.length());
	}

}
